    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        // boton buscar
        String codigoBuscado = codigoDeBarrasLabel.getText().trim(); //el codigo buscado es igual al texto que salga del label

    if (codigoBuscado.isEmpty()) { //si esta vacio muestra un pane de alerta 
        JOptionPane.showMessageDialog(this, "Ingresa un codigo valido");
        return;
    }

    DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
    modelo.setRowCount(0); //tabla limpiada

    boolean encontrado = false; //falso porque aun no se encuentra nada

    for (Producto producto : inventario) {
        if (producto.getCodigo().equalsIgnoreCase(codigoBuscado)) { //condicion de busqueda, si el producto del label es igual a codgioBUscado
            modelo.addRow(new Object[]{
                producto.getCodigo(),
                producto.getNombre(),
                producto.getCategoria(),
                producto.getPrecio(),
                producto.getDescuento(),
                producto.getPrecioFinal()
            });
            encontrado = true;
            break; 
        }
    }

    if (!encontrado) {
        JOptionPane.showMessageDialog(this, "No se encontro.");
    }

    }//GEN-LAST:event_buscarButtonActionPerformed

    private void eliminarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarButtonActionPerformed
        //boton eliminar
           String codigoBuscado = codigoDeBarrasLabel.getText().trim();

    if (codigoBuscado.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Codigo de barras a eliminar.");
        return;
    }

    Producto productoAEliminar = null;

    for (Producto p : inventario) {
        if (p.getCodigo().equalsIgnoreCase(codigoBuscado)) {
            productoAEliminar = p;
            break;
        }
    }

    if (productoAEliminar == null) {
        JOptionPane.showMessageDialog(this, "No existe el prodcuto.");
        return;
    }

    int confirmacion = JOptionPane.showConfirmDialog(this, "Quiere eliminar el producto: " + productoAEliminar.getNombre() + "?", "Confirmar eliminacion",JOptionPane.YES_NO_OPTION);

    if (confirmacion == JOptionPane.YES_OPTION) {inventario.remove(productoAEliminar); //borra del inventario

        // tabla cambiada
        imprimirOrdenButtonActionPerformed(null); 
        JOptionPane.showMessageDialog(this, "El producto se eliminó de manera correcta.");
    }

    }//GEN-LAST:event_eliminarButtonActionPerformed

