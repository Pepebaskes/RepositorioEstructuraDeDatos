    private void imprimirInversoButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_imprimirInversoButtonActionPerformed
    
    DefaultTableModel modelo = (DefaultTableModel) tabla.getModel();
    modelo.setRowCount(0); //tabla limpada

    //duplicamos la lista que ya tenemos de inventario 
    List<Producto> productosInvertidos = new ArrayList<>(inventario);
    Collections.reverse(productosInvertidos); //metddo en el que ordenará los productos al reves

    //mostramos los productos desde el arreglo de manera invertida
    for (Producto producto : productosInvertidos) {
        modelo.addRow(new Object[]{
            producto.getCodigo(),
            producto.getNombre(),
            producto.getCategoria(),
            producto.getPrecio(),
            producto.getDescuento(),
            producto.getPrecioFinal()
        });
    }


    }//GEN-LAST:event_imprimirInversoButtonActionPerformed

